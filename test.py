# 大寫英文字A~Z的ASCII是 65~90, 小寫是97~122
x = input("輸入一個英文字:")

if 65 <= ord(x) <= 90:  # 大寫
    # A是大寫第"1"個字母, A的ASCII是65, 65減64="1"
    # B是大寫第2個字母, A的ASCII是66, 66減64=2
    # C是大寫第3個字母, A的ASCII是67, 67減64=3
    i = ord(x) - 64     # ord(x)把字母轉成ASCII
    print(x ,"是大寫第", i, "個字母")
elif 97 <= ord(x) <= 122:   # 小寫
    # a是小寫第"1"個字母, a的ASCII是97, 97減96="1"
    # b是小寫第2個字母, b的ASCII是98, 98減96=2
    # f是小寫第6個字母, f的ASCII是102, 102減96=6
    i = ord(x) - 96
    print(x, "是小寫弟", i, "個字母")
